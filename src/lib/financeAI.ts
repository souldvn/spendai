    analysis += "‚Ä¢ –û—Ç–∫–ª–∞–¥—ã–≤–∞–π—Ç–µ –º–∏–Ω–∏–º—É–º 20% –¥–æ—Ö–æ–¥–∞ –Ω–∞ —Å–±–µ—Ä–µ–∂–µ–Ω–∏—è\n";
